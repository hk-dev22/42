#include <unistd.h>

void	ft_is_negative(int n)
{
	char t;
	char f;

	t = 'P';
	f = 'N';
	if (n < 0)
	{
		write(1, &f, 1);
	}
	else
	{
		write(1, &t, 1);
	}
}
